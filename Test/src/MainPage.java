    private ArrayList getSearchResult(){
        ArrayList searchResult = new ArrayList();
        searchResult.add("Niniew");
        searchResult.add("Win");
        searchResult.add("Romantic");
        searchResult.add("Action");
        searchResult.add("Drama");
        searchResult.add("Fiction");
        searchResult.add("comedy");
        return searchResult;
    }
    
    private void bindData() {
        getSearchResult().stream().forEach((searchResult) -> { 
            defaultListModel.addElement(searchResult);
    });
        searchList.setModel(defaultListModel);
        searchList.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    private void searchFilter (String searchTerm){
        DefaultListModel fillteredItems = new DefaultListModel();
        ArrayList searchResult = getSearchResult();
        
        searchResult.stream().forEach((s) -> {    
            String srName = searchResult.toString().toLowerCase();
            if (srName.contains(searchTerm.toLowerCase())){
                fillteredItems.addElement(s);
            }
            defaultListModel  = fillteredItems;
            searchList.setModel(defaultListModel);
        });
    }
    
    private void addPlaceholderStyle(JTextField searchBar){
        Font font = searchBar.getFont();
        font = font.deriveFont(Font.ITALIC);
        searchBar.setFont(font);
        searchBar.setForeground(Color.gray);
    }
    
    private void removePlaceholderStyle(JTextField searchBar){
        Font font = searchBar.getFont();
        font = font.deriveFont(Font.PLAIN | Font.BOLD);
        searchBar.setFont(font);
        searchBar.setForeground(Color.GRAY);
    }
